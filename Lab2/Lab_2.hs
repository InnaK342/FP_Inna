--Завдання 1. Послiдовнiсть тотожних елементiв списку замiнити одним елементом,напр.: [1,1,1,5,5,3, 1,1,222,222,222,222] ) [1,5,3,1,222].

f1 :: Eq a => [a] -> [a]
f1 [] = []
f1 [x] = [x]
f1 (x1:x2:xs) = if x1 == x2 then f1(x2:xs) else x1:f1(x2:xs)
--Результат: f1 [1,1,1,2,1,22,22,3] - > [1,2,1,22,3]


f1_2:: (Eq a) => [a] -> [a] 
f1_2 (x:xs)
  | xs == []       = [x]
  | x == head (xs) = f1_2 xs
  | otherwise      = x : f1_2 xs
--Результат: f1_2 [1,1,1,2,1,22,22,3] - > [1,2,1,22,3]

