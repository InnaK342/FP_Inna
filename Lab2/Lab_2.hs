gcd' :: (Integer, Integer) -> Integer
gcd' (x1, 0)  =  x1
gcd' (x1, x2) =  gcd' (x2, (x1 `mod` x2))

f2 :: (Integer, Integer) -> Bool
f2 (x1,x2) = gcd' (x1, x2) == 1
--Результат: f2 (2,4) - > False
